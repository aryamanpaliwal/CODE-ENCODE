class Solution{
    public:
    long long maximumProfit(vector<long long>&a, int n) {
        // Code here
        long long res=0;
        for(int i=1;i<n;i++)
        {
            if(a[i]>a[i-1])
            {
                res+=(a[i]-a[i-1]);
            }
        }
        return res;
    }
};

